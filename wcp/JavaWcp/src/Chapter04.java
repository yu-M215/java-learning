
public class Chapter04 {
	public static void main(String[] args) {
		// 自分の名前を「String」型の変数に代入して出力
		String str = "ゆう";
		System.out.println(str);
		// 好きな言葉を同じ変数に再代入して出力
		str = "花鳥風月";
		System.out.println(str);
		// ラッキーナンバーとして「int」型で宣言した定数を出力
		final int LUCKY_NUMBER = 7;
		System.out.println(LUCKY_NUMBER);
	}
}
